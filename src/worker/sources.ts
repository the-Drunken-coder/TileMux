import type { ProviderSecretName } from "./env";
import type { CachePolicy } from "./utils/http";

export type ProviderKind = "debug-grid" | "remote-xyz" | "r2-xyz" | "pmtiles-r2";
export type SourceKind = ProviderKind;
export type SourceFormat = "raster" | "vector";

export type DebugGridProvider = {
  kind: "debug-grid";
};

export type RemoteXyzProvider = {
  kind: "remote-xyz";
  template: string;
  browserTileTemplate?: string;
  secretPlaceholders?: Record<string, ProviderSecretName>;
  requestHeaders?: Record<string, string>;
};

export type R2XyzProvider = {
  kind: "r2-xyz";
  r2KeyTemplate: string;
};

export type PmtilesR2Provider = {
  kind: "pmtiles-r2";
  r2Key: string;
};

export type ProviderConfig =
  | DebugGridProvider
  | RemoteXyzProvider
  | R2XyzProvider
  | PmtilesR2Provider;

export type SourceTransform = {
  kind: "invert-raster";
};

export type BaseSource = {
  id: string;
  name: string;
  provider: ProviderConfig;
  format: SourceFormat;
  tileSize: number;
  minzoom: number;
  maxzoom: number;
  sourceMaxzoom?: number;
  bounds?: readonly [number, number, number, number];
  ext: string;
  attribution?: string;
  cachePolicy: CachePolicy;
  cacheTtlSeconds?: number;
  enabled?: boolean;
  transforms?: SourceTransform[];
  style?: Record<string, unknown>;
};

export type DebugGridSource = BaseSource & {
  provider: DebugGridProvider;
  format: "raster";
};

export type RemoteXyzSource = BaseSource & {
  provider: RemoteXyzProvider;
};

export type R2XyzSource = BaseSource & {
  provider: R2XyzProvider;
};

export type PmtilesR2Source = BaseSource & {
  provider: PmtilesR2Provider;
};

export type TileSource =
  | DebugGridSource
  | RemoteXyzSource
  | R2XyzSource
  | PmtilesR2Source;

export type SanitizedSource = Pick<
  TileSource,
  | "id"
  | "name"
  | "format"
  | "tileSize"
  | "minzoom"
  | "maxzoom"
  | "sourceMaxzoom"
  | "bounds"
  | "ext"
  | "attribution"
  | "cachePolicy"
> & {
  kind: ProviderKind;
  browserTileTemplate?: string;
  supportsTileJson: boolean;
  supportsGeneratedStyle: boolean;
};

const TILEMUX_USER_AGENT =
  "TileMux/0.0 (+https://github.com/the-Drunken-coder/TileMux)";

const DEFAULT_REMOTE_HEADERS = {
  "User-Agent": TILEMUX_USER_AGENT,
};

type RemoteRasterSourceOptions = {
  id: string;
  name: string;
  template: string;
  maxzoom: number;
  sourceMaxzoom?: number;
  ext?: "png" | "jpg";
  attribution: string;
  browserTileTemplate?: string;
  secretPlaceholders?: Record<string, ProviderSecretName>;
};

function remoteRasterSource({
  id,
  name,
  template,
  maxzoom,
  sourceMaxzoom,
  ext = "png",
  attribution,
  browserTileTemplate,
  secretPlaceholders,
}: RemoteRasterSourceOptions): RemoteXyzSource {
  return {
    id,
    name,
    provider: {
      kind: "remote-xyz",
      template,
      browserTileTemplate,
      secretPlaceholders,
      requestHeaders: DEFAULT_REMOTE_HEADERS,
    },
    format: "raster",
    tileSize: 256,
    minzoom: 0,
    sourceMaxzoom,
    maxzoom,
    ext,
    attribution,
    cachePolicy: "respect-upstream",
    enabled: true,
  };
}

