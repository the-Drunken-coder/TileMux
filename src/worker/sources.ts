export function isSourceEnabled(source: TileSource): boolean {
  return source.enabled !== false;
}

export function getSource(sourceId: string): TileSource | undefined {
  return Object.values(SOURCES).find((source) => source.id === sourceId);
}

export function getEnabledSource(sourceId: string): TileSource | undefined {
  const source = getSource(sourceId);
  return source && isSourceEnabled(source) ? source : undefined;
}

export function listEnabledSources(): TileSource[] {
  return Object.values(SOURCES).filter(isSourceEnabled);
}

export function sanitizeSource(source: TileSource): SanitizedSource {
  return {
    id: source.id,
    name: source.name,
    kind: source.provider.kind,
    format: source.format,
    tileSize: source.tileSize,
    minzoom: source.minzoom,
    maxzoom: source.maxzoom,
    sourceMaxzoom: source.sourceMaxzoom,
    bounds: source.bounds,
    ext: source.ext,
    attribution: source.attribution,
    cachePolicy: source.cachePolicy,
    browserTileTemplate:
      source.provider.kind === "remote-xyz"
        ? source.provider.browserTileTemplate
        : undefined,
    supportsTileJson: source.provider.kind !== "pmtiles-r2",
    supportsGeneratedStyle: source.format === "raster" || Boolean(source.style),
  };
}
